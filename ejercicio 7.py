#Solicite dos números e indique cuál es mayor o si son iguales.
numero1 = float(input("Ingrese su primer numero: "))
numero2 = float(input("Ingrese su segundo numero: "))

if numero1 > numero2:
    print ("su primer numero es el mayor")
elif numero1 < numero2:
    print ("su segundo numero es el mayor")
else:
    print ("los dos numeros ingresados son iguales")

